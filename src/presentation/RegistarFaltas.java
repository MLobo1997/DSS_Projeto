    public void updateListAlunos(Turno t){
        DefaultListModel<String> lista = new DefaultListModel<>();
        try{
            for(Aluno a : t.getAlunos()){
                lista.addElement(a.getUsername() + "\t" + a.getNome());
            }
        }
        catch (Exception e){
            e.getMessage();
        }
        jList1.setModel(lista);
    }
    

